package selenium_code;

public class Assert {
    public static void main(String[] args) {

        Assert asser=new Assert();

        //assertEqaul
        asser.assertEqual(Expected,actual);

        //assertNotEqual
        asser.assertNotEqual(Expected,actual);

        //null
        asser.assertNull();

        //not null
        asser.assertNotNull();

        //True
        asser.assertTrue();

        //false
        asser.assertFalse();

    }
}
